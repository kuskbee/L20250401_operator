#include <iostream>

using namespace std;

int main()
{
	int IntegerX = 70;
	int IntegerY = 30;

	cout << IntegerX << " + "<< IntegerY << " = " << IntegerX + IntegerY << endl;
	cout << IntegerX << " - "<< IntegerY << " = " << IntegerX - IntegerY << endl;
	cout << IntegerX << " * "<< IntegerY << " = " << IntegerX * IntegerY << endl;
	cout << IntegerX << " / "<< IntegerY << " = " << IntegerX / IntegerY << endl;

	float FloatX = 1.10;
	float FloatY = 2.30;

	cout << FloatX << " + " << FloatY << " = " << FloatX + FloatY << endl;
	cout << FloatX << " - " << FloatY << " = " << FloatX - FloatY << endl;
	cout << FloatX << " * " << FloatY << " = " << FloatX * FloatY << endl;
	cout << FloatX << " / " << FloatY << " = " << FloatX / FloatY << endl;
}